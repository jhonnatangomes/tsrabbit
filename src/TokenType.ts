export enum TokenType {
  // Single character tokens.
  LEFT_PAREN,
  RIGHT_PAREN,
  LEFT_BRACKET,
  RIGHT_BRACKET,
  LEFT_BRACE,
  RIGHT_BRACE,
  COMMA,
  DOT,
  QUESTION,
  COLON,
  SEMICOLON,

  // One or two character tokens.
  PLUS,
  PLUS_PLUS,
  PLUS_EQUAL,
  MINUS,
  MINUS_MINUS,
  MINUS_EQUAL,
  STAR,
  STAR_EQUAL,
  SLASH,
  SLASH_SLASH,
  SLASH_EQUAL,
  BANG,
  BANG_EQUAL,
  EQUAL,
  EQUAL_EQUAL,
  GREATER,
  GREATER_EQUAL,
  LESS,
  LESS_EQUAL,
  OR,
  AND,

  // Literals.
  IDENTIFIER,
  STRING_LITERAL,
  NUMBER_LITERAL,

  // Keywords.
  CLASS,
  ELSE,
  EXTENDS,
  FALSE,
  FOR,
  IF,
  MAP,
  NULL,
  NUMBER,
  RETURN,
  STRING,
  SUPER,
  THIS,
  TRUE,
  WHILE,

  EOF,
}
